package entities;

public class Category {
    int id;
    String categoryName;

    public Category(int id, String categoryName) {
        this.categoryName = categoryName;
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCategoryName() {
        return categoryName;
    }

    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }
}
